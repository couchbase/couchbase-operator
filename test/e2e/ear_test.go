func mustGenerateKeyCertPair(t *testing.T, keyPassphrase string) ([]byte, []byte, error) {
	kpr := e2eutil.CreateKeyPairReqData(e2eutil.KeyTypeRSA, e2eutil.KeyEncodingPKCS8, e2eutil.CertTypeClient, e2eutil.CreateCertReq("Couchbase Cluster"))

	validFrom := time.Now().In(time.UTC)
	validTo := validFrom.AddDate(10, 0, 0)

	ca, err := e2eutil.NewCertificateAuthority(e2eutil.KeyTypeRSA, "Couchbase CA", validFrom, validTo, e2eutil.CertTypeCA)
	if err != nil {
		e2eutil.Die(t, fmt.Errorf("failed to create certificate authority: %w", err))
	}

	_, clusterKey, clusterCert, err := kpr.Generate(ca, validFrom, validTo)
	if err != nil {
		e2eutil.Die(t, fmt.Errorf("failed to generate key cert pair: %w", err))
	}

	if clusterKey, err = e2eutil.AddPKCS8Passphrase(clusterKey, []byte(keyPassphrase)); err != nil {
		e2eutil.Die(t, fmt.Errorf("failed to add passphrase to key: %w", err))
	}

	return clusterKey, clusterCert, nil
}

func mustCreateAutoGeneratedKMIPKey(t *testing.T, k8s *types.Cluster, name string) (*couchbasev2.CouchbaseEncryptionKey, *corev1.Secret) {
	testKeyPassphrase := "test-passphrase"

	clusterKey, clusterCert, err := mustGenerateKeyCertPair(t, testKeyPassphrase)
	if err != nil {
		e2eutil.Die(t, fmt.Errorf("failed to create key cert pair: %w", err))
	}

	secretName := name + "-secret"
	secret := &corev1.Secret{
		ObjectMeta: metav1.ObjectMeta{
			Name: secretName,
		},
		Data: map[string][]byte{
			"passphrase": []byte(testKeyPassphrase),
			"tls.key":    clusterKey,
			"tls.crt":    clusterCert,
		},
	}

	secret = e2eutil.MustCreateSecret(t, k8s, secret)

	key := &couchbasev2.CouchbaseEncryptionKey{
		ObjectMeta: metav1.ObjectMeta{
			Name: name,
		},
		Spec: couchbasev2.CouchbaseEncryptionKeySpec{
			KeyType: couchbasev2.CouchbaseEncryptionKeyTypeKMIP,
			KMIPKey: &couchbasev2.CouchbaseEncryptionKeyKMIP{
				ClientSecret:          secretName,
				Host:                  "localhost",
				Port:                  1636,
				TimeoutInMs:           1000,
				VerifyWithSystemCA:    true,
				VerifyWithCouchbaseCA: true,
				EncryptionApproach:    couchbasev2.CouchbaseEncryptionApproachNativeEncryptDecrypt,
			},
		},
	}

	key, err = k8s.CRClient.CouchbaseV2().CouchbaseEncryptionKeys(k8s.Namespace).Create(context.TODO(), key, metav1.CreateOptions{})
	if err != nil {
		e2eutil.Die(t, fmt.Errorf("failed to create encryption key %s: %w", name, err))
	}

	return key, secret
}

