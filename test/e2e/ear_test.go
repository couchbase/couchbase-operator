// mustCreateEncryptedBucketEncryptionKey creates an auto-generated encryption key for bucket encryption testing.
func mustCreateEncryptedBucketEncryptionKey(t *testing.T, k8s *types.Cluster, name string) *couchbasev2.CouchbaseEncryptionKey {
	key := &couchbasev2.CouchbaseEncryptionKey{
		ObjectMeta: metav1.ObjectMeta{
			Name: name,
		},
		Spec: couchbasev2.CouchbaseEncryptionKeySpec{
			KeyType:       couchbasev2.CouchbaseEncryptionKeyTypeAutoGenerated,
			AutoGenerated: &couchbasev2.CouchbaseEncryptionKeyAutoGenerated{},
			Usage: &couchbasev2.CouchbaseEncryptionKeyUsage{
				Configuration:          true,
				Key:                    true,
				Log:                    true,
				Audit:                  true,
				AllBuckets:             false, // We want to specifically assign this key to bucket
				ManagedBucketSelection: true,  // Allow selection for managed buckets
			},
		},
	}

	createdKey, err := k8s.CRClient.CouchbaseV2().CouchbaseEncryptionKeys(k8s.Namespace).Create(context.TODO(), key, metav1.CreateOptions{})
	if err != nil {
		e2eutil.Die(t, fmt.Errorf("failed to create encryption key %s: %w", name, err))
	}

	return createdKey
}

// Tests creation of a cluster with encryption at rest and an encrypted bucket
// 1. Creates an auto-generated encryption key
// 2. Creates a Couchbase cluster with encryption at rest enabled
// 3. Creates a bucket that uses the encryption key
// 4. Validates that the cluster, key, and bucket are properly created.
// 5. Validates that we can edit encryption settings.
func TestCreateEncryptedBucket(t *testing.T) {
	// Platform configuration.
	f := framework.Global

	kubernetes, cleanup := f.SetupTest(t)
	defer cleanup()

	// Encryption at rest requires Couchbase Server 8.0+
	framework.Requires(t, kubernetes).AtLeastVersion("8.0.0")

	// Static configuration.
	clusterSize := 3

	// Create the cluster with encryption at rest enabled
	cluster := clusterOptions().WithEphemeralTopology(clusterSize).Generate(kubernetes)
	cluster.Spec.Security.EncryptionAtRest = &couchbasev2.EncryptionAtRestSpec{
		Managed: true,
		Configuration: &couchbasev2.EncryptionAtRestUsageConfiguration{
			Enabled: true, // Required for auto-generated keys
		},
	}

	cluster = e2eutil.MustNewClusterFromSpec(t, kubernetes, cluster)

	// Create encryption key
	encryptionKeyName := "test-bucket-encryption-key"
	key := mustCreateEncryptedBucketEncryptionKey(t, kubernetes, encryptionKeyName)

	// Wait for the encryption key to be created in the cluster
	e2eutil.MustWaitForClusterEvent(t, kubernetes, cluster, e2eutil.NewEncryptionKeyCreatedEvent(cluster, key.Name), 2*time.Minute)
	e2eutil.MustAssertEncryptionKeyExists(t, kubernetes, cluster, key.Name)

	// Create an encrypted bucket using the encryption key
	bucket := e2espec.DefaultBucket()
	bucket.Name = "test-encrypted-bucket"
	bucket.Spec.Replicas = 0
	bucket.Spec.EncryptionAtRest = &couchbasev2.BucketEncryptionAtRestConfiguration{
		KeyName: encryptionKeyName,
	}

	createdBucket := e2eutil.MustNewBucket(t, kubernetes, bucket)
	e2eutil.MustWaitUntilBucketExists(t, kubernetes, cluster, createdBucket, 2*time.Minute)

	keyID := e2eutil.MustGetEncryptionKeyID(t, kubernetes, cluster, encryptionKeyName)

	e2eutil.MustPatchBucketInfo(t, kubernetes, cluster, bucket.GetName(), jsonpatch.NewPatchSet().Test("/EncryptionAtRestKeyId", &keyID), time.Minute)

	rotationInterval := time.Hour * 24 * 11
	rotationIntervalSeconds := int(rotationInterval.Seconds())
	createdBucket = e2eutil.MustPatchBucket(t, kubernetes, createdBucket, jsonpatch.NewPatchSet().Replace("/spec/encryptionAtRest/rotationInterval", &metav1.Duration{Duration: rotationInterval}), time.Minute)
	e2eutil.MustPatchBucketInfo(t, kubernetes, cluster, createdBucket.GetName(), jsonpatch.NewPatchSet().Test("/EncryptionAtRestDekRotationInterval", &rotationIntervalSeconds), 2*time.Minute)

	lifetime := time.Hour * 24 * 44
	lifetimeSeconds := int(lifetime.Seconds())
	createdBucket = e2eutil.MustPatchBucket(t, kubernetes, createdBucket, jsonpatch.NewPatchSet().Replace("/spec/encryptionAtRest/keyLifetime", &metav1.Duration{Duration: lifetime}), time.Minute)
	e2eutil.MustPatchBucketInfo(t, kubernetes, cluster, createdBucket.GetName(), jsonpatch.NewPatchSet().Test("/EncryptionAtRestDekLifetime", &lifetimeSeconds), 2*time.Minute)

	// Check the events match what we expect:
	// * Cluster created
	// * Encryption key created
	// * Bucket created
	expectedEvents := []eventschema.Validatable{
		e2eutil.ClusterCreateSequence(clusterSize),
		eventschema.Event{Reason: k8sutil.EventReasonEncryptionKeyCreated},
		eventschema.Event{Reason: k8sutil.EventReasonBucketCreated},
		// Once the bucket is created the encryption key needs to add a usage for the bucket
		eventschema.Event{Reason: k8sutil.EventReasonEncryptionKeyUpdated},
		// Once the usage is added the bucket can use the key
		eventschema.Event{Reason: k8sutil.EventReasonBucketEdited},

		eventschema.Repeat{Times: 2, Validator: eventschema.Event{Reason: k8sutil.EventReasonBucketEdited}},
	}
	ValidateEvents(t, kubernetes, cluster, expectedEvents)
}
