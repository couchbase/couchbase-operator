func validateKMIPKeyAndPassphrase(t *testing.T, kubernetes *types.Cluster, cluster *couchbasev2.CouchbaseCluster, keyName string, secret *corev1.Secret) {
	expectedKeyPath := "/opt/couchbase/var/lib/couchbase/key/" + secret.Name + "-tls.key"
	expectedCertPath := "/opt/couchbase/var/lib/couchbase/key/" + secret.Name + "-tls.crt"

	e2eutil.MustPatchEncryptionKeySettings(t, kubernetes, cluster, keyName, jsonpatch.NewPatchSet().Test("/EncryptionKey/KMIP/KeyPath", expectedKeyPath).Test("/EncryptionKey/KMIP/CertPath", expectedCertPath), 1*time.Minute)

	podName := couchbaseutil.CreateMemberName(cluster.Name, 0)
	mustCheckFileContents(t, kubernetes, podName, string(secret.Data["tls.key"]), expectedKeyPath)
	mustCheckFileContents(t, kubernetes, podName, string(secret.Data["tls.crt"]), expectedCertPath)
}

func mustCheckFileContents(t *testing.T, k8s *types.Cluster, podName string, content string, filePath string) {
	err := retryutil.RetryFor(2*time.Minute, func() error {
		cmd := "cat " + filePath
		stdout, _, err := e2eutil.ExecShellInPod(k8s, podName, cmd)

		if err != nil {
			return err
		}

		// Ignore possible trailing newlines
		if len(content) == (len(stdout)+1) && content[len(content)-1] == '\n' {
			content = content[:len(content)-1]
		}

		if content != stdout {
			return fmt.Errorf("file contents do not match: %s", stdout)
		}

		return nil
	})

	if err != nil {
		e2eutil.Die(t, fmt.Errorf("file contents check failed: %w", err))
	}
}
