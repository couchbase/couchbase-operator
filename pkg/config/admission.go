// getAdmissionDeployment returns the canonical deployment for the admission controller.
func (o *generateAdmissionOptions) getAdmissionDeployment() *appsv1.Deployment {
	replicas := int32(o.replicas)

	nonRoot := true

	deployment := &appsv1.Deployment{
		ObjectMeta: metav1.ObjectMeta{
			Name: AdmissionResourceName,
		},
		Spec: appsv1.DeploymentSpec{
			Replicas: &replicas,
			Selector: &metav1.LabelSelector{
				MatchLabels: map[string]string{
					"app.kubernetes.io/name":     AdmissionResourceName,
					"app.kubernetes.io/instance": "couchbase-admission",
				},
			},
			Template: corev1.PodTemplateSpec{
				ObjectMeta: metav1.ObjectMeta{
					Labels: map[string]string{
						"app.kubernetes.io/name":      AdmissionResourceName,
						"app.kubernetes.io/instance":  "couchbase-admission",
						"app.kubernetes.io/component": "admission-webhook",
						"app.kubernetes.io/part-of":   "couchbase",
					},
				},
				Spec: corev1.PodSpec{
					ServiceAccountName: AdmissionResourceName,
					SecurityContext: &corev1.PodSecurityContext{
						RunAsNonRoot: &nonRoot,
						SeccompProfile: &corev1.SeccompProfile{
							Type: corev1.SeccompProfileTypeRuntimeDefault,
						},
					},
					Containers: []corev1.Container{
						{
							Name:            AdmissionResourceName,
							Image:           o.image,
							ImagePullPolicy: corev1.PullPolicy(o.imagePullPolicy),
							SecurityContext: &corev1.SecurityContext{
								AllowPrivilegeEscalation: func() *bool { b := false; return &b }(),
								ReadOnlyRootFilesystem:   func() *bool { b := true; return &b }(),
								Capabilities: &corev1.Capabilities{
									Drop: []corev1.Capability{"ALL"},
								},
							},
							Command: []string{
								"couchbase-admission-controller",
							},
							Args: []string{
								"-zap-log-level=" + o.logLevel.value,
								"-tls-cert-file=" + "/var/run/secrets/couchbase.com/couchbase-operator-admission/tls.crt",
								"-tls-private-key-file=" + "/var/run/secrets/couchbase.com/couchbase-operator-admission/tls.key",
								"-validate-secrets=" + strconv.FormatBool(o.validateSecrets),
								"-validate-storage-classes=" + strconv.FormatBool(o.validateStorageClasses),
							},
							Ports: []corev1.ContainerPort{
								{
									Name:          "https",
									ContainerPort: 8443,
								},
							},
							ReadinessProbe: &corev1.Probe{
								ProbeHandler: corev1.ProbeHandler{
									HTTPGet: &corev1.HTTPGetAction{
										Path:   "/readyz",
										Port:   intstr.FromString("https"),
										Scheme: corev1.URISchemeHTTPS,
									},
								},
							},
							LivenessProbe: &corev1.Probe{
								ProbeHandler: corev1.ProbeHandler{
									HTTPGet: &corev1.HTTPGetAction{
										Path:   "/readyz",
										Port:   intstr.FromString("https"),
										Scheme: corev1.URISchemeHTTPS,
									},
								},
								InitialDelaySeconds: 30,
								TimeoutSeconds:      5,
								PeriodSeconds:       30,
								FailureThreshold:    3,
							},
							VolumeMounts: []corev1.VolumeMount{
								{
									Name:      AdmissionResourceName,
									MountPath: "/var/run/secrets/couchbase.com/couchbase-operator-admission",
									ReadOnly:  true,
								},
							},
						},
					},
					Volumes: []corev1.Volume{
						{
							Name: AdmissionResourceName,
							VolumeSource: corev1.VolumeSource{
								Secret: &corev1.SecretVolumeSource{
									SecretName: AdmissionResourceName,
								},
							},
						},
					},
				},
			},
		},
	}

	if o.withResources {
		deployment.Spec.Template.Spec.Containers[0].Resources = corev1.ResourceRequirements{
			Requests: corev1.ResourceList{
				corev1.ResourceCPU:    o.cpuRequest.value,
				corev1.ResourceMemory: o.memoryRequest.value,
			},
			Limits: corev1.ResourceList{
				corev1.ResourceCPU:    o.cpuLimit.value,
				corev1.ResourceMemory: o.memoryLimit.value,
			},
		}
	}

	for _, secret := range o.imagePullSecret.imagePullSecrets {
		reference := corev1.LocalObjectReference{
			Name: secret,
		}

		deployment.Spec.Template.Spec.ImagePullSecrets = append(deployment.Spec.Template.Spec.ImagePullSecrets, reference)
	}

	return deployment
}

