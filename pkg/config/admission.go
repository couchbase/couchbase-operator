// validate performs any validation that cobra doesn't on options.
func (o *generateAdmissionOptions) validate(command string) error {
	if o.scope.value.isNamespaceScope() && o.namespaceSelector.LabelSelector == nil {
		if command != UpdateCmd && command != DeleteCmd {
			return fmt.Errorf("dynamic admission controller with namespace scope requires the --namespace-selector flag")
		}
	}

	// When namespaced, we cannot access namespaces or storage classes
	// (or any cluster scoped resource for that matter) so force these
	// to false to cause the role to never be added, and the DAC not to
	// bother checking them.
	if o.scope.value.isNamespaceScope() {
		o.validateStorageClasses = false
	}

	return nil
}

type AdmissionCerts struct {
	key  []byte
	cert []byte
	ca   *util_x509.CertificateAuthority
}

func GenerateAdmissionCerts(namespace string, duration time.Duration) (*AdmissionCerts, error) {
	// Generate TLS configuration for the dynamic admission controller.
	validFrom := time.Now()
	validTo := time.Now().Add(duration)

	ca, err := util_x509.NewCertificateAuthority(util_x509.KeyTypeRSA, AdmissionResourceName+" CA", validFrom, validTo, util_x509.CertTypeCA)
	if err != nil {
		return nil, err
	}

	req := util_x509.KeyPairRequest{
		KeyType:   util_x509.KeyTypeRSA,
		CertType:  util_x509.CertTypeServer,
		ValidFrom: validFrom,
		ValidTo:   validTo,
		Req: &x509.CertificateRequest{
			Subject: pkix.Name{
				CommonName: AdmissionResourceName,
			},
			DNSNames: []string{
				AdmissionResourceName + "." + namespace + ".svc",
			},
		},
	}

	key, cert, err := req.Generate(ca)
	if err != nil {
		return nil, err
	}

	return &AdmissionCerts{key: key, cert: cert, ca: ca}, nil
}

// generate dumps all admission controller resources to standard out.
func (o *generateAdmissionOptions) generate(flags *genericclioptions.ConfigFlags) ([]runtime.Object, error) {
	namespace, _, err := flags.ToRawKubeConfigLoader().Namespace()
	if err != nil {
		return nil, err
	}

	bundle, err := GenerateAdmissionCerts(namespace, defaultExpirationTime)
	if err != nil {
		return nil, err
	}

	resources := []runtime.Object{
		o.getAdmissionServiceAccount(),
		o.getAdmissionRole(),
		o.getAdmissionRoleBinding(namespace),
		o.getAdmissionSecret(bundle.key, bundle.cert),
		o.getAdmissionDeployment(),
		o.getAdmissionService(),
		o.getAdmissionValidatingWebhook(namespace, bundle.ca.Certificate),
	}

	return resources, nil
}

// getAdmissionRole return the (cluster) role to run with.
func (o *generateAdmissionOptions) getAdmissionRole() runtime.Object {
	metadata := metav1.ObjectMeta{
		Name: AdmissionResourceName,
	}

	rules := []rbacv1.PolicyRule{
		{
			APIGroups: []string{
				couchbasev2.GroupName,
			},
			Resources: []string{
				couchbasev2.ClusterCRDResourcePlural,
				couchbasev2.BucketCRDResourcePlural,
				couchbasev2.EphemeralBucketCRDResourcePlural,
				couchbasev2.MemcachedBucketCRDResourcePlural,
				couchbasev2.ReplicationCRDResourcePlural,
				couchbasev2.UserCRDResourcePlural,
				couchbasev2.GroupCRDResourcePlural,
				couchbasev2.RoleBindingCRDResourcePlural,
				couchbasev2.BackupCRDResourcePlural,
				couchbasev2.BackupRestoreCRDResourcePlural,
				couchbasev2.AutoscalerCRDResourcePlural,
				couchbasev2.CollectionCRDResourcePlural,
				couchbasev2.CollectionGroupCRDResourcePlural,
				couchbasev2.ScopeCRDResourcePlural,
				couchbasev2.ScopeGroupCRDResourcePlural,
				couchbasev2.MigrationReplicationCRDResourcePlural,
				couchbasev2.EncryptionKeyCRDResourcePlural,
			},
			Verbs: []string{
				"get",
				"list",
			},
		},
	}

	if o.validateSecrets {
		rules = append(rules, rbacv1.PolicyRule{
			APIGroups: []string{
				"",
			},
			Resources: []string{
				"secrets",
			},
			Verbs: []string{
				"get",
			},
		})
	}

	if o.scope.value.isClusterScope() {
		if o.validateStorageClasses {
			rules = append(rules, rbacv1.PolicyRule{
				APIGroups: []string{
					"storage.k8s.io",
				},
				Resources: []string{
					"storageclasses",
				},
				Verbs: []string{
					"get",
					"list", // used to check that a default storageClass exists in the cluster
				},
			})
		}

		return &rbacv1.ClusterRole{
			ObjectMeta: metadata,
			Rules:      rules,
		}
	}

	return &rbacv1.Role{
		ObjectMeta: metadata,
		Rules:      rules,
	}
}

// getAdmissionServiceAccount get the service account to run as.
func (o *generateAdmissionOptions) getAdmissionServiceAccount() *corev1.ServiceAccount {
	return &corev1.ServiceAccount{
		ObjectMeta: metav1.ObjectMeta{
			Name: AdmissionResourceName,
		},
	}
}

// getAdmissionRoleBinding generates the (cluster) role binding linking the role to the service account.
func (o *generateAdmissionOptions) getAdmissionRoleBinding(namespace string) runtime.Object {
	metadata := metav1.ObjectMeta{
		Name: AdmissionResourceName,
	}

	subjects := []rbacv1.Subject{
		{
			Kind:      "ServiceAccount",
			Name:      AdmissionResourceName,
			Namespace: namespace,
		},
	}

	if o.scope.value.isClusterScope() {
		return &rbacv1.ClusterRoleBinding{
			ObjectMeta: metadata,
			Subjects:   subjects,
			RoleRef: rbacv1.RoleRef{
				APIGroup: "rbac.authorization.k8s.io",
				Kind:     "ClusterRole",
				Name:     AdmissionResourceName,
			},
		}
	}

	return &rbacv1.RoleBinding{
		ObjectMeta: metadata,
		Subjects:   subjects,
		RoleRef: rbacv1.RoleRef{
			APIGroup: "rbac.authorization.k8s.io",
			Kind:     "Role",
			Name:     AdmissionResourceName,
		},
	}
}

// getAdmissionSecret generates the TLS secret providing server certificates.
